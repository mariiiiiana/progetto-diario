function inferSemantic(raw) {
  const k = deaccent(raw);
  const rules = [
    [/^ansiet[aà] per (?:la|il|lo|l')(.+)$/i, m => {
      if (/vita personale/i.test(m[1])) return 'personal life worries';
      if (/peso/i.test(m[1])) return 'weight worries';
      if (/esame/i.test(m[1])) return 'exam nerves';
      return null;
    }],
    [/^aspirazione a (.+)$/i, m => /vita/i.test(m[1]) ? 'wanting a healthier life' : null],
    [/^incapacit[aà] di (.+)$/i, m => /esprimersi/i.test(m[1]) ? 'unable to express myself' : null],
    [/^dissociazione dalla (.+)$/i, m => /realt/i.test(m[1]) ? 'disconnected from reality' : null],
    [/^dissonanza tra (.+) e (.+)$/i, m => /realt/i.test(m[0]) ? 'mirror vs reality' : null],
    [/^colpa e (.+)$/i, m => /autocritica/i.test(m[1]) ? 'guilt and self-blame' : null],
    [/^immagine di s[eé]$/i, () => 'self-image'],
    [/^abbandono dell(?:a|')(.+)$/i, m => /universit/i.test(m[1]) ? 'dropping out of college' : null],
    [/^volont[aà] di (.+)$/i, m => /cambiamento/i.test(m[1]) ? 'will to change' : null],
    [/^decisione (.+)$/i, m => /difficile/i.test(m[1]) ? 'hard decision' : null],
    [/^problema (.+)$/i, m => /complicato/i.test(m[1]) ? 'complicated situation' : null],
    [/^soluzione (.+)$/i, m => /inaspettata/i.test(m[1]) ? 'unexpected way out' : null],
  ];
  for (const [re, fn] of rules) {
    const m = raw.match(re);
    if (m) {
      const out = fn(m);
      if (out) return out;
    }
  }
  return null;
}

